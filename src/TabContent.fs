namespace Reactstrap

open Fable.Core
open Fable.Core.JsInterop
open Fable.React
open Reactstrap
open Fable.React.Props

[<RequireQualifiedAccess>]
module TabContent =

    type TabContentProps =
        | Tag of U2<string, obj>
        | ActiveTab of U2<string, int>
        | CssModule of CSSModule
        | Custom of IHTMLProp list

    let tabContent (props: TabContentProps seq) (elems: ReactElement seq): ReactElement =
        let customProps =
            props
            |> Seq.collect (function
                | Custom props -> props
                | _ -> List.empty)
            |> keyValueList CaseRules.LowerFirst

        let typeProps =
            props
            |> Seq.choose (function
                | Custom _ -> None
                | prop -> Some prop)
            |> keyValueList CaseRules.LowerFirst

        let props = JS.Constructors.Object.assign (createEmpty, customProps, typeProps)
        ofImport "TabContent" "reactstrap" props elems
